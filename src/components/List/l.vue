<h5>浏览器缓存机制</h5>
浏览器的缓存机制分为强制缓存和协商缓存
浏览器向服务器一个http请求，首先会向浏览器缓存中查找
  如果没有数据结果和缓存标识，强制缓存失败，想服务器发送http请求；
  如果查找数据，但是数据已失效，浏览器带着缓存标识发送http请求，由服务器来决定是否使用缓存数据，如果服务器的数据没有更新，则会返回304，我们则
  取浏览器缓存中读取数据，返回给浏览器，如果服务器的数据更新了，则返回新的数据和200状态，新的数据将存入浏览器缓存中
  
