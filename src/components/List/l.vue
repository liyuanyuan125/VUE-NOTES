<h5>闭包及闭包的使用场景</h5>
>闭包的概念：
  当一个函数执行的时候会形成一个私有的的上下文作用域，用于保护和保存私有变量这种机制叫做闭包；

  正常的情况下，一个函数执行完成后，就会立即会释放，但是如果在上下文中有个变量被占用，从而形成了当前的上下文不能被销毁
  函数在执行的时候，会形成一个上下文，这个函数可能会被释放，也可能不被释放，无论释放与否他的作用分别是保护和保存
  保护：保护自己的私有变量不被外界干扰
  保存：如果上下文不被释放，上下文定义的变量可以理解为保存了下来，可以供下级调取使用
>闭包的形成
 函数的嵌套，函数被外层引用
>闭包应用场景
settimeout延时回调，匿名函数
>闭包缺点：
 闭包导致函数中的数量一直存在内存中，不被释放，导致内存泄漏；
